long	ft_atoi(char *str)
{
	int		i;
	long	num;

	num = 0;
	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (int)(str[i] - '0');
		i++;
	}
	return (num);
}
